package EjemplosTema2;

public class TiposDatos {
    /**
     * En esta clase vamos a ver ejemplos de declaracion de variables
     */
    public static void main(String[] args) {
        //Declaramos variables para almacenar números enteros
        int edad;
        long habitantes;
        //Doy valores a estas variables
        edad =0;
        habitantes = 0;

        edad = 18;
        habitantes = 525000;
        //Mostramos por pantalla
        System.out.println(edad);
        System.out.println(habitantes);
        //Declaramos variables para almacenar números reales o con parte decimal
        double saldoCuenta = 0.0;
        float estatura = (float) 0.0;//En metros
        float nota = 6.5f;

        saldoCuenta = 1000.8;
        estatura = 1.83f;
        nota = 5.5f;
        
        //Declaramos variables de tipo lógico
        boolean cierto = true;
        boolean terminado = false;
        boolean salir = false;
        //Declaramos variables de tipo carácter(1 carácter)
        char letra;
        char grupo;
        //Variables para almacenar cadenas de caracteres(varios caracteres)
        String nombre;
        String apellidos;
        String dni;

        
    }
}
