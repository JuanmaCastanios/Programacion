package EjerciciosPDFTema2;

public class Intercambio {
    public static void main(String[] args) {
        int a = 4, b = 7, aux;
        System.out.println("a = " + a);
        System.out.println("b = " + b +"\n");
        aux = a;
        a = b;
        b = aux;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
