    private void botonCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonCrearMouseClicked
        Contacto c = new Contacto(JOptionPane.showInputDialog(null, "Nombre: ", "CONTACTO", 1));
        String telefono = JOptionPane.showInputDialog(null, "Telefono: ", "CONTACTO", 1);
        if(!telefono.equals("")){
            c.setTelefono(Integer.parseInt(telefono));
        }
        listaContactos.add(c);
        c.setIdContacto(listaContactos.indexOf(c));
    }//GEN-LAST:event_botonCrearMouseClicked

