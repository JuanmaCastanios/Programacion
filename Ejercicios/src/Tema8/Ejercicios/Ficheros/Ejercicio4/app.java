    public void lecturaEscrituraBinaria(){
        File carta = new File("C:\\Ejercicios\\Programacion\\Ejercicios\\src\\Tema8\\Ejercicios\\Ficheros\\Ejercicio4\\carta" + contCarta + ".txt");;
        boolean eof;
        try (DataInputStream dis = new DataInputStream(new BufferedInputStream(new FileInputStream(empleados)));
            DataOutputStream dos = new DataOutputStream(new BufferedOutputStream(new FileOutputStream(carta, true)))){
            System.out.println(carta.getAbsolutePath());
            eof = false;
            while(!eof){
                dos.writeChar(dis.readChar());
            }
            
        } catch (EOFException ex) {
            contCarta ++;
        } catch (IOException ex){
            
        }
    }
    
    public File seleccionarFile(String modo) {

        JFileChooser jfc = new JFileChooser("C:\\Ejercicios\\Programacion\\Ejercicios\\src\\Tema8\\Ejercicios\\Ficheros\\Ejercicio4");
        jfc.setDialogTitle("Elije el directorio");
        File file = null; //referencia al archivo
        int resp = jfc.showDialog(this, (modo.equals("r")?"Abrir":"Cerrar"));
        if (resp == JFileChooser.APPROVE_OPTION) {
            file = jfc.getSelectedFile();
        }
        return file;
    }
    
