package EjemplosBucles;

// Programa que muestre por consola 10 veces "hola"

public class Ejemplo1 {
    public static void main(String[] args) throws Exception {
        int i = 0;
        while(i < 10){
            System.out.println("Hola");
            i++;
        }
        System.out.println("Adios...");
    }
}
