    //Devolvermos la matriz con los datos de la JTable
    private Object[][] getDatos() {
        Object[][] obj = null;

        try (Connection con = DriverManager.getConnection(url, "root", "")) {
            //ahora recuperao los metadatos de la tabla
            try (Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE)) {
                String sql = "SELECT * FROM alumnos";
                try (ResultSet rs = st.executeQuery(sql)) {
                    ResultSetMetaData rsmd = rs.getMetaData(); //recupero los metadatos de la tabla1

                    rs.last();
                    int numCols = rsmd.getColumnCount();
                    int numFils = rs.getRow();

                    obj = new Object[numFils][numCols];
                    setColumnas(rsmd);
                    int j = 0;
                    rs.beforeFirst();
                    while (rs.next()) {
                        for (int i = 0; i < numCols; i++) {
                            obj[j][i] = rs.getObject(i + 1);
                        }
                        j++;
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(EjemploTable.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (SQLException ex) {
                Logger.getLogger(EjemploTable.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EjemploTable.class.getName()).log(Level.SEVERE, null, ex);
        }
        return obj;
    }

    //Establecemos los nombres de las columnas de la JTable
    private void setColumnas(ResultSetMetaData rsmd) {
        try {
            columnas=new String[rsmd.getColumnCount()];
            for(int i=1;i<=rsmd.getColumnCount();i++){
                columnas[i-1]=rsmd.getColumnLabel(i);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EjemploTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

