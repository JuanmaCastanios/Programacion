    private void recuperarMetaDatos() {
        try ( Connection con = DriverManager.getConnection(url, "root", "");) {
            dbmd = con.getMetaData(); //recuperamos los metadatos de la bd
            //le uso para
            nombre_bd = dbmd.getURL();
            nombre_usuario = dbmd.getUserName();

            //ahora recuperao los metadatos de la tabla
            try ( Statement st = con.createStatement();) {
                String sql = "SELECT * FROM alumnos";
                try ( ResultSet rs = st.executeQuery(sql);) {
                    rsmd = rs.getMetaData(); //recupero los metadatos de la tabla1
                    
                    nombre_tabla = rsmd.getTableName(1);
                    //creo una etiqueta para cada columna y le añado al panel correspondiente de la ventana
                    String separador = "\t-\t"; //separador de columnas
                    for (int i = 1; i <= rsmd.getColumnCount(); i++) {
                        if (i == rsmd.getColumnCount()) {
                            separador = ""; //para que no saque el guión en la última columna
                        }
                        JLabel label = new JLabel(rsmd.getColumnLabel(i) + separador); //creo la etiqueta
                        panelColumnas.add(label); //la añado al panel para que se vea
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

