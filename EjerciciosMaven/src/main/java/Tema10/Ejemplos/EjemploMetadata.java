    //Muestra los tipos de las columnas en la caja de texto. Para ello usa los metadatos de la tabla.
    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        try {
            String info = "";
            String separador = " - ";
            for (int i = 1; i <= rsmd.getColumnCount(); i++) {
                if (i == rsmd.getColumnCount()) {
                    separador = "";
                }
                String tipo = String.valueOf(rsmd.getColumnTypeName(i)) + separador;
                info += tipo;
            }
            this.jTextField1.setText(info);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

