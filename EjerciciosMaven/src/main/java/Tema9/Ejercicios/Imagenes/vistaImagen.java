    private File[] seleccionarFiles() {

        JFileChooser jfc = new JFileChooser(".\\src\\main\\java\\Tema9\\Ejercicios\\Imagenes\\Resources");
        jfc.setDialogTitle("Elije las im√°genes");
        jfc.setMultiSelectionEnabled(true);
        File[] lista = null;
        int resp = jfc.showOpenDialog(this);
        if (resp == JFileChooser.APPROVE_OPTION) {
            lista = jfc.getSelectedFiles();
            t.start();
        }
        return lista;
    }

