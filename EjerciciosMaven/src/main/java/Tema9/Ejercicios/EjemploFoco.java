    private void pruebas(){
        this.jButton2.requestFocus();
        //Asociar Listener a la caja de texto para controlar eventos de focus
        this.jTextField1.addFocusListener(this);
        //Asociar Listener para capturar la pulsaci√≥n enter y provocar el click
        
        KeyListener listener = new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                
            }

            @Override
            public void keyPressed(KeyEvent e) {
                if(e.getKeyCode() == KeyEvent.VK_ENTER){
                    getjButton1().doClick();
                }
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        };
        this.jButton1.addKeyListener(listener);
        
        //Asignar Listener a los botones para capturar su click
        ControladorVentana controlador = new ControladorVentana(this);
        this.jButton1.addActionListener(controlador);
        this.jButton2.addActionListener(controlador);
        
    }

